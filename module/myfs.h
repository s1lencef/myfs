/*
 * myfs.h — общие определения для модуля файловой системы MYFS.
 *
 * Этот заголовок описывает on-disk структуры (то, как данные лежат на
 * блочном устройстве) и определения ioctl-команд. Файл сделан так, чтобы
 * его можно было включать как из кода ядра, так и из userspace-программы:
 * для этого типы выбраны фиксированной ширины (__u32/__u64 и т.п.), а
 * заголовки подключаются через #ifdef __KERNEL__.
 *
 * Логика разметки диска:
 *
 *   сектор 0                — первая копия суперблока (struct myfs_super_block)
 *   сектор sb_copy_offset   — вторая (резервная) копия суперблока
 *   далее                   — по одному "файлу" на каждый оставшийся участок
 *                             диска. Каждый файл занимает file_size_sectors
 *                             секторов и создаётся при инициализации ФС.
 *
 * Каждый файл предваряется маленьким заголовком (struct myfs_file_header),
 * который лежит в первом секторе файла и хранит метаинформацию: длину
 * полезных данных и контрольную сумму (хеш) этих данных.
 */

#ifndef _MYFS_H
#define _MYFS_H

#ifdef __KERNEL__
#include <linux/types.h>
#include <linux/ioctl.h>
#else
/* В userspace берём системные определения __u8/__u16/__u32/__u64 из
 * <linux/types.h> — те же, что использует ядро. Это гарантирует, что
 * структуры совпадают байт в байт, и не конфликтует с другими системными
 * заголовками (например <sys/stat.h>). */
#include <linux/types.h>
#include <sys/ioctl.h>
#endif

/* Размер сектора. Мы фиксируем его в 512 байт — это «классический»
 * логический размер сектора, с которым удобно работать. Реальное блочное
 * устройство тоже адресуется блоками; мы оперируем единицей в 512 байт. */
#define MYFS_SECTOR_SIZE        512u

/* Магическое число для опознавания нашей ФС в суперблоке.
 * "MYFS" в виде ASCII плюс версия. */
#define MYFS_MAGIC              0x4D594653u   /* 'M''Y''F''S' */

/* Версия формата on-disk. */
#define MYFS_VERSION            1u

/* Ограничения на параметры (защита от заведомо неадекватных значений,
 * переданных как аргументы модуля). */
#define MYFS_MAX_NAME_LIMIT     255u   /* верхняя граница для max_name_len */
#define MYFS_MAX_FILESZ_LIMIT   4096u  /* верхняя граница для M (в секторах) */

/*
 * Суперблок на диске.
 *
 * Хранится в начале нулевого сектора (первая копия) и в начале сектора
 * с номером sb_copy_offset (вторая копия). Всё, что в нём лежит, нужно
 * для того, чтобы при монтировании однозначно восстановить геометрию ФС.
 *
 * Поле checksum считается по всей структуре, в которой само поле checksum
 * предварительно обнулено. Это позволяет проверять целостность суперблока.
 */
struct myfs_super_block {
	__u32 magic;             /* MYFS_MAGIC — признак нашей ФС */
	__u32 version;           /* версия формата (MYFS_VERSION) */

	__u64 total_sectors;     /* всего секторов на устройстве */
	__u64 sb_copy_offset;    /* номер сектора со 2-й копией суперблока */

	__u32 max_name_len;      /* максимальная длина имени файла */
	__u32 file_size_sectors; /* размер каждого файла в секторах (M) */

	__u64 first_data_sector; /* номер первого сектора с данными (файлами) */
	__u64 file_count;        /* сколько файлов создано в ФС */

	__u32 checksum;          /* контрольная сумма самого суперблока */
	__u32 reserved;          /* выравнивание/задел на будущее */
} __attribute__((packed));

/*
 * Заголовок одного файла.
 *
 * Лежит в самом начале первого сектора, отведённого под файл. Полезные
 * данные файла идут сразу после заголовка (внутри тех же секторов файла).
 *
 * data_checksum — контрольная сумма полезных данных файла (по data_len
 * байтам). Используется для команды ioctl «получить метаинформацию».
 */
struct myfs_file_header {
	__u32 magic;          /* MYFS_MAGIC — пометка валидного файла */
	__u32 data_len;       /* длина полезных данных в байтах */
	__u32 data_checksum;  /* хеш полезных данных */
	__u32 reserved;       /* выравнивание */
} __attribute__((packed));

/* Полезная ёмкость одного файла в байтах при заданном размере в секторах.
 * Из общего объёма вычитаем место под заголовок файла. */
#define MYFS_FILE_PAYLOAD(file_size_sectors) \
	((file_size_sectors) * MYFS_SECTOR_SIZE - sizeof(struct myfs_file_header))

/* ---------------------------------------------------------------------------
 * Определения IOCTL.
 *
 * IOCTL вызываются на ОТКРЫТОМ ФАЙЛЕ нашей ФС (любой файл подойдёт — команда
 * работает над всей ФС, кроме маппинга, который относится к конкретному файлу,
 * передаваемому аргументом).
 * ------------------------------------------------------------------------- */

#define MYFS_IOC_MAGIC 'F'

/* Структура для запроса маппинга секторов конкретного файла.
 * Пользователь заполняет file_index, ядро возвращает first_sector и
 * sector_count (диапазон секторов на устройстве, занятый этим файлом). */
struct myfs_sector_map {
	__u64 file_index;    /* [in]  индекс файла (0..file_count-1) */
	__u64 first_sector;  /* [out] первый сектор файла на устройстве */
	__u64 sector_count;  /* [out] сколько секторов занимает файл */
};

/* Один элемент списка метаинформации (для команды «получить все хеши»). */
struct myfs_meta_entry {
	__u64 file_index;    /* индекс файла */
	__u32 data_len;      /* длина полезных данных */
	__u32 data_checksum; /* хеш полезных данных */
};

/* Структура для получения списка метаинформации обо всех файлах.
 * Пользователь выделяет массив entries на capacity элементов и передаёт
 * указатель; ядро заполняет count фактически записанных элементов. */
struct myfs_meta_list {
	__u64 capacity;                  /* [in]  сколько элементов влезает */
	__u64 count;                     /* [out] сколько реально записано */
	__u64 entries_ptr;               /* [in]  указатель на массив (как __u64) */
};

/* Команды: */

/* Обнулить полезные данные всех файлов (data_len=0, checksum пересчитан). */
#define MYFS_IOC_ZERO_ALL      _IO(MYFS_IOC_MAGIC, 1)

/* Стереть ФС: затереть оба суперблока (после этого ФС не смонтируется). */
#define MYFS_IOC_ERASE_FS      _IO(MYFS_IOC_MAGIC, 2)

/* Получить список метаинформации (хеши) по всем файлам. */
#define MYFS_IOC_GET_META      _IOWR(MYFS_IOC_MAGIC, 3, struct myfs_meta_list)

/* Получить маппинг секторов для заданного файла. */
#define MYFS_IOC_GET_MAP       _IOWR(MYFS_IOC_MAGIC, 4, struct myfs_sector_map)

#endif /* _MYFS_H */
